#include "3-calc.h"
/**
 * op_add - adds two values
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_add(int a, int b);
{
	return (a + b);
}
/**
 * op_sub - subtracts two values
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_sub(int a, int b);
{
	return (a - b);
}
/**
 * op_mul - multiplies two values
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_mul(int a, int b);
{
	return (a * b);
}
/**
 * op_div - divides two values
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_div(int a, int b);
{
	return (a / b);
}
/**
 * op_mod - Returns the remainder two values
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_mod(int a, int b);
{
	return (a % b);
}
